function isBackdrop(element, dialog) {
  if (!element)
    return false;
  const backdrop = element.getAttribute("data-backdrop");
  if (backdrop == null)
    return false;
  if (backdrop === "")
    return true;
  if (backdrop === "true")
    return true;
  const id = dialog == null ? void 0 : dialog.id;
  if (!id)
    return false;
  return backdrop === id;
}


